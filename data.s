; Constants stored as memory words
MAX_PATIENTS    DCD 3
PATIENT_SIZE    DCD 44
BILL_SIZE       DCD 16

; Arrays
PATIENT_ARRAY   EQU 0x20000000   
BILLING_ARRAY    SPACE 16 * 3   ;

;buffer

HR_BUFFER      SPACE 4 * 10 * 3   ; 120 bytes
BP_BUFFER      SPACE 4 * 10 * 3
O2_BUFFER      SPACE 4 * 10 * 3

